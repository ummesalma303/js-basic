// normal addition vs shorthand addition
// normal addition
var apple =20;
var apple = apple + 10;
console.log(apple);
// shorthand addition
var apple =20;
apple += 10;
console.log(apple);

// normal subtraction
var apple =20;
var apple = apple + 10;
console.log(apple);
// shorthand subtraction
var apple =20;
apple += 10;
console.log(apple);

// normal multiplication
var apple =20;
var apple = apple * 10;
console.log(apple);
// shorthand multiplication
var apple =20;
apple *= 10;
console.log(apple);

// normal divider
var apple =20;
var apple = apple / 10;
console.log(apple);
// shorthand divider
var apple =20;
apple /= 10;
console.log(apple);


// normal reminder
var apple =20;
var apple = apple % 10;
console.log(apple);
// shorthand reminder
var apple =20;
apple %= 10;
console.log(apple);

