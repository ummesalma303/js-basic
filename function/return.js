function tenTimes(number) {
    const result = number * 10;
    return result
}
const output = tenTimes(5);
const bigNumber = tenTimes(15);
console.log('output:',output, bigNumber);


function cutHalf(number) {
    const half = number / 2;
    return half
}
const half = cutHalf(5);
const halfNum = cutHalf(10);
console.log(half,halfNum);
